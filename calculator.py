# Defining a function that multiplies two numbers
def multiply(a,b):
    return a * b


# Defining a function that adds two numbers
def add(a,b):
    return a + b

# Defining a function that subtracts two numbers
def subtract(a,b):
    return a - b


# Defining a function that divides two numbers
def divide(a,b):
    return a/b

